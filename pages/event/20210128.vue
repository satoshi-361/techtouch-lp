<script lang="ts">
import { Component, Vue } from 'nuxt-property-decorator'
import axios from 'axios'
import { Payload } from '../../lambda/contact-api/src/seminar/types'

@Component
export default class Contact extends Vue {
  submitted = false

  data: Payload & { captchaResponse: string } = {
    purpose: 'ウェビナーに参加',
    companyName: '',
    divisionName: '',
    position: '',
    contactName: '',
    email: '',
    phoneNumber: '',
    numberOfEmployees: '',
    captchaResponse: '',
    channel: '',
    partner: '',
    type: '入力ミス8割減！開発不要のシステムUI改善'
  }

  meta = {
    title: 'ウェビナー申し込み',
    description: 'ウェビナーへのお申し込みはこちらからお願いします',
    type: 'article',
    url: 'https://techtouch.jp/event/20210128',
    image: 'https://techtouch.jp/ogp.png'
  }

  head() {
    return {
      title: this.meta.title,
      meta: [
        {
          hid: 'description',
          name: 'description',
          content: this.meta.description
        },
        { hid: 'og:type', property: 'og:type', content: this.meta.type },
        { hid: 'og:title', property: 'og:title', content: this.meta.title },
        {
          hid: 'og:description',
          property: 'og:description',
          content: this.meta.description
        },
        { hid: 'og:url', property: 'og:url', content: this.meta.url },
        { hid: 'og:image', property: 'og:image', content: this.meta.image }
      ]
    }
  }

  get disabled() {
    // 必須項目を入力後、送信可能にする
    return (
      !this.data.purpose ||
      !this.data.companyName ||
      !this.data.divisionName ||
      !this.data.position ||
      !this.data.contactName ||
      !this.data.email ||
      !this.data.numberOfEmployees ||
      !this.data.captchaResponse ||
      this.data.channel === 'パートナー企業からの紹介' && !this.data.partner
    )
  }

  get mailError() {
    // メールアドレスのフォーマットになっているか
    // 参照 https://emailregex.com/
    const regexp = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
    return this.submitted && !regexp.exec(this.data.email)
      ? 'メールアドレスを正しく入力してください'
      : ''
  }

  get phoneError() {
    // 電話番号は0から始まる && 9,10桁のみ
    const regexp = /^0[0-9]{9,10}$/
    return this.submitted && (this.data.phoneNumber && !regexp.exec(this.data.phoneNumber))
      ? '電話番号を正しく入力してください'
      : ''
  }
  onCaptchaSuccess(token) {
    this.data.captchaResponse = token
  }
  async submit() {
    this.submitted = true
    if (!this.mailError && !this.phoneError) {
      const res = await axios.post(
        'https://o7jqp835ze.execute-api.ap-northeast-1.amazonaws.com/production/seminar',
        this.data
      )
      // @ts-ignore
      this.$router.push('/thanks-event')
    }
  }
}
</script>

