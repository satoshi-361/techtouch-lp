            <h3>テックタッチを導入した経緯について教えてください</h3>
            <p>旧基幹システムは約20年前にスクラッチ開発したものでアミューズメント施設ならではの機能も網羅していました。ただ、さすがに老朽化している面があり、現在のビジネススタイルに対応させ、業務の効率化を進めるため、刷新することに決めました。</p>
            <p>旧システムは、利用者目線で見ると、慣れもあり、だれかに聞けば教えてもらえるといった環境でした。一方、新しいシステムはERPパッケージソフトを一部カスタマイズしたもので、導入に合わせてペーパーレス化をはじめ業務フローを最適化した部分も多く、またWebシステムなので操作手順や使い勝手も従来とはまるで異なります。利用者はエンジニア的な経験や知識を持っているわけではなく、PCやWebシステムに不慣れな者も少なくありません。また、開発・運営側の私たちも、新システムに関して完全に習熟しているとは言いがたい状況です。</p>
            <p>かといって新しいシステムに慣れるまでに時間をかけてしまうと、現場の業務に支障を来してしまうだけでなく、会社全体の業務も滞ってしまいかねません。</p>
            <p>当初はこのような課題を解決する決定打はなく、いわゆる操作画面のキャプチャと説明文を付記した操作手順マニュアルを用意して、何らかの研修を実施するつもりでした。しかし、展示会でテックタッチのデモンストレーションを見たとき、直感的ではありますが、これは新システムの利用促進や負担軽減に有用だと考え、すぐに導入の検討を開始し、デモ版で動作確認をした上で導入を決めました。</p>
