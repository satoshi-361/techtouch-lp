            <p>当社では、約20年ぶりに新しく導入した基幹システムの操作ガイドにテックタッチを利用しています。システムの機能は約50種類ありますが、手順をわかりやすくするため個別の作業フローに分けて、約200の操作ガイドを用意しています。</p>
            <h3>操作ガイド作成の体制について教えてください</h3>
            <p>今回はリリース前のシステムなのでガイド作成を外部に任せるのは難しく、社内で作成しました。プログラミングは不要で、プレゼンテーションソフトでオブジェクトやアニメーションを作るときのように、システムを実際に操作しながら作成できるので、慣れればテックタッチの操作自体は難しいことはありません。テックタッチの条件分岐機能などを使えばより複雑なガイドも作れると思いますが、今回はシステムも新しく、テックタッチの利用も初めてなので、分かりやすいようシンプルさを重視しました。</p>
            <p>一方、より網羅的で、システムの操作方法だけでなく業務フローもカバーするガイドとするため、ボリュームは当初の予定より増やしました。期間は、専任担当者1名で、約3か月間となります。</p>

            <h2 id="index02">操作マニュアルや研修会なしで、新システムの利用を促進</h2>
            <figure>
