            <h3>テックタッチの導入効果について教えてください</h3>
            <p>コロナ禍により対面での研修が難しくなっていた中、基幹システムの刷新にあたり、スムーズなシステムの定着・習熟度向上といった成果につながり、現場だけでなく運営側の負担も大いに軽減することができました。旧システムを導入した20年前の現場対応は、今回とは比較にならないほど大変だったと聞いています。各効果の詳細は次の通りです。</p>
            <h4>【効果1】現場（利用者）の負担軽減</h4>
            <p>新システムに対して、現場の負担や戸惑いがまったくなかったというわけにはいきませんが、操作ガイドに対する評価は高く、利用者アンケートを見ると約8割が「操作ガイドが用意されていて良かった」と答えています。操作マニュアルの閲覧や研修会などに時間や手間を割くことなく、新システムを短期間で使えるようになったのは、テックタッチ導入の重要な成果であることは間違いありません。</p>
            <p>また、決算時の作業など、通常の作業とは異なる業務にも操作ガイドがあることで、現場の負担が軽減されたと考えています。研修を受けた人しか使えず属人化したり、指導に時間と手間を割かなければならないといった事態を防ぐことにもつながると期待しています。</p>
            <h4>【効果2】運営側の負担軽減</h4>
            <p>今回はガイドを作ったので操作マニュアルは作っていません。研修・教育などの手間もほとんどかかっておらず、操作に関する問い合わせなどに対する運営側の負担も最小限に抑えることができました。システムが仕様変更になった場合でも、テックタッチのガイドはレイアウトの変更などには自動で追従するため、マニュアルを作り直して配布したり再研修などを行ったりする必要がないのも、大きな成果だと捉えています。</p>
            <h4>【効果3】システム利用の習熟度が向上・見える化</h4>
            <p>テックタッチの利用履歴を分析すると、操作ガイドの利用頻度は時間の経過にともない減少傾向にあります。システムへの入力頻度が下がったわけではないので、システム利用の習熟度が向上していると解釈できます。これまで感覚でしか理解できなかった利用状況がこのように可視化されることで、現場での利用状況も客観的に把握できました。今後、システムの利用しやすさや機能向上にも役立てることができると考えています。</p>
            <h4>【効果4】コロナ禍でのスムーズなシステム導入</h4>
            <p>コロナウイルス感染拡大による影響が大きな時期に、新システムをカットオーバーせざるを得なかったのですが、集合研修や現地に出向いてのサポートが難しい状況でも、スムーズに移行することができたと感じています。結果論ですが、テックタッチのおかげでウイルス感染のリスクも必要最低限に抑えられたと思います。</p>
            <h3>操作ガイドだけで、すぐに新システムを使えるようになったのでしょうか？</h3>
