              <li>入力制限や入力ルールのチェックができるので、漏れやミスの削減につながる</li>
              <li>操作ガイドの開発や修正が容易で、社内で対応できる</li>
              <li>基幹システム（Webシステム）に影響（修正）や負担がかからない</li>
              <li>クラウドサービスなので、導入や運用の負荷がかからない</li>
